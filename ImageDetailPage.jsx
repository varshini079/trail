function ImageDetailPage() {
  const { wardrobe, index } = useParams();
  const isWardrobe1 = wardrobe === 'wardrobe1';
  const itemData = isWardrobe1 ? itemData1 : itemData2;
  const clickedImage = itemData[index];

  return (
    <Box sx={{ display: 'flex', justifyContent: 'space-between', padding: 2 }}>
      <Box sx={{ width: '30%', height: '80vh', overflowY: 'scroll', bgcolor: '#f5f5f5', padding: 2 }}>
        <h2>Other Images</h2>
        <ImageList cols={1} gap={8}>
          {itemData
            .filter((_, i) => i !== parseInt(index))
            .map((item, i) => (
              <ImageListItem key={item.img} onClick={() => window.location.href = `/detail/${wardrobe}/${i}`} sx={{ cursor: 'pointer' }}>
                <img
                  srcSet={`${item.img}?w=100&fit=crop&auto=format&dpr=2 2x`}
                  src={`${item.img}?w=100&fit=crop&auto=format`}
                  alt={item.title}
                  loading="lazy"
                />
              </ImageListItem>
            ))}
        </ImageList>
      </Box>
      <Box sx={{ width: '65%', display: 'flex', flexDirection: 'column', alignItems: 'center' }}>
        <img
          src={clickedImage.img}
          alt={clickedImage.title}
          style={{ maxWidth: '100%', maxHeight: '80vh', objectFit: 'contain' }}
        />
        <h3>{clickedImage.title}</h3>
      </Box>
    </Box>
  );
}

